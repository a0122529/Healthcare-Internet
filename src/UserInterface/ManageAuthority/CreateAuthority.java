    private void createAuthorityjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAuthorityjButtonActionPerformed
        String authName = authorityNamejTextField.getText().trim();
        String username = userjTextField1.getText().trim();
        String password = passwordjTextField.getText().trim();
        String city = cityjTextField.getText().trim();
        String emailId = emailIdjTextField.getText().trim();
        String state = "aaa ";
        String street1 = "bbb";
        String street2 = "ccc";
        String zip = "1100223";
        if (authName.equals("") || authName == null || username.equals("") || username == null || password.equals("") || password == null
                || state.equals("") || state == null) {
            JOptionPane.showMessageDialog(null, "Please fill all the details to create an authority");
            return;
        }
        String validName = ecosystem.getUcd().validUsernameCheck(username);
        if (validName == (null)) {
            JOptionPane.showMessageDialog(null, "Username already taken, please enter a different username.");
            return;
        }

        Authority authority = ecosystem.addAuthority(authName, emailId, state);
        Employee employee = authority.getPersonDirectory().getEmployeeDirectory().createEmployee(authName, street1, street2, city, state, zip);
        UserAccount userAccount = authority.getUserAccountDirectory().createUserAccount(username, password, employee, new AuthorityRole());
        ecosystem.getUcd().addUsername(username);
    }//GEN-LAST:event_createAuthorityjButtonActionPerformed

