    private void removejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removejButtonActionPerformed
        int selectedRow = entpTypeNamejTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to remove");
            return;
        }
        Enterprise enterprise  = (Enterprise) entpTypeNamejTable.getValueAt(selectedRow, 1);
        for(Network network : ecosystem.getNetworkLst()) {
            network.getEnterpriseDirectory().getEnterpriseList().remove(enterprise);
        }
        Network network = (Network) cityNamejComboBox1.getSelectedItem();
        populatEntNameTable(network);
    }//GEN-LAST:event_removejButtonActionPerformed

