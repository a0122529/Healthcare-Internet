    private void populateCityComboBox() {
        cityNamejComboBox1.removeAllItems();
        for (Network network : ecosystem.getNetworkLst()) {
            cityNamejComboBox1.addItem(network);
            populatEntNameTable(network);
        }
    }

    private void populatEntNameTable(Network network) {
        DefaultTableModel model = (DefaultTableModel) entpTypeNamejTable.getModel();
        model.setRowCount(0);
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[4];
                row[0] = enterprise.getEnterpriseType().getValue();
                row[1] = enterprise;
                row[2] = enterprise.getOrgId();
                row[3] = enterprise.getLocation().getCity();
                model.addRow(row);
            }
        }
