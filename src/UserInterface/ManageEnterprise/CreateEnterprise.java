    private void createEnterpricejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpricejButtonActionPerformed
        Network network = (Network) cityNamejComboBox.getSelectedItem();
        Enterprise.EnterpriseType enterpriseType = (Enterprise.EnterpriseType) enterpriseTypeComboBox.getSelectedItem();
        String enterpriseName = enterpriseNamejTextFiel.getText().trim();
        String street1 = streetLine1jTextField.getText().trim();
        String street2 = streetLine2jTextField.getText().trim();
        String state = statejTextField.getText().trim();
        String city = cityjTextField.getText().trim();
        String zip = zipCodejTextField.getText().trim();
        if (network == (null) || enterpriseType == (null) || enterpriseName.equals("") || enterpriseName == null
                || street1.equals("") || street1 == null || state.equals("") || state == null || city.equals("") || city == null
                || zip.equals("") || zip == null) {
            JOptionPane.showMessageDialog(null, "Please add all the data");
            return;
        }
        network.getEnterpriseDirectory().addEnterprise(enterpriseName, enterpriseType, street1, street2,
                state, city, zip);

    }//GEN-LAST:event_createEnterpricejButtonActionPerformed

    private void backjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backjButtonActionPerformed
        upc.remove(this);
        CardLayout cardLayout = (CardLayout) upc.getLayout();
        cardLayout.previous(upc);
    }//GEN-LAST:event_backjButtonActionPerformed


