    // Here we are creating a method to populate the table using the below method 
    private void populateCityTable() {
        DefaultTableModel model = (DefaultTableModel) cityjTable.getModel();
        model.setRowCount(0);

        for (Network network : ecosystem.getNetworkLst()) {
            Object[] row = new Object[2];
            row[0] = network.getName();
            row[1] = network.getCityId();
            model.addRow(row);
        }
    }

