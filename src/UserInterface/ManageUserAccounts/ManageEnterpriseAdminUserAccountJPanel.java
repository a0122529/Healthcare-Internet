    private void createAdminjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAdminjButtonActionPerformed
        Network network = (Network) cityNamejComboBox.getSelectedItem();
        Enterprise enterprise = (Enterprise) enterpriseNamejComboBox.getSelectedItem();
        String username = userNamejTextField.getText().trim();
        String password = passwordjTextField.getText().trim();
        String adminName = adminNamejTextField.getText().trim();
        String street1 = streetLine1jTextField.getText().trim();
        String street2 = streetLine2jTextField.getText().trim();
        String city = cityjTextField.getText().trim();
        String state = statejTextField.getText().trim();
        String zipcode = zipCodejTextField.getText().trim();
        if (network == null || enterprise == null || username.equals("") ||username==null|| password.equals("")||password== null
                || adminName.equals("")||adminName == null || street1.equals("") || street1==null  ||  city.equals("")|| city ==null
                || state.equals("") || zipcode.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill all the details to create admin account");
            return;
        }
        String validName = ecosystem.getUcd().validUsernameCheck(username);
        if (validName == (null)) {
            JOptionPane.showMessageDialog(null, "Username already taken, please enter a different username.");
            return;
        }

        Employee employee = enterprise.getPersonDirectory().getEmployeeDirectory().createEmployee(adminName, street1,
                street2, city, state, zipcode);
        if (enterprise.getEnterpriseType().getValue().equals("Hospital")) {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(username,
                    password, employee, new HospitalAdminRole());
        } else if (enterprise.getEnterpriseType().getValue().equals("Clinic")) {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(username,
                    password, employee, new ClinicAdminRole());
        } else if (enterprise.getEnterpriseType().getValue().equals("Pharmacy")) {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(username,
                    password, employee, new PharmacyAdminRole());
        } else {
            UserAccount userAccount = enterprise.getUserAccountDirectory().createUserAccount(username,
                    password, employee, new PharmaceuticalAdminRole());
        }
        populateAdminDetailsTable(network);
        ecosystem.getUcd().addUsername(username);


    }//GEN-LAST:event_createAdminjButtonActionPerformed

