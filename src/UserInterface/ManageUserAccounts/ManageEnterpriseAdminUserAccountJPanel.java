    private void removejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removejButtonActionPerformed
        int selectedRow = adminDetailsjTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to remove");
        }
        UserAccount ua = (UserAccount) adminDetailsjTable.getValueAt(selectedRow, 1);
        for (Network network : ecosystem.getNetworkLst()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                enterprise.getUserAccountDirectory().getUserAccountList().remove(ua);
                Network network1 = (Network) cityNamejComboBox.getSelectedItem();
                populateAdminDetailsTable(network1);

            }
        }
    }//GEN-LAST:event_removejButtonActionPerformed

