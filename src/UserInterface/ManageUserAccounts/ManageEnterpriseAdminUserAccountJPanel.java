    private void populateCityComboBox() {
        cityNamejComboBox.removeAllItems();
        for (Network network : ecosystem.getNetworkLst()) {
            if(network!= null){
            cityNamejComboBox.addItem(network);
            populateAdminDetailsTable(network);
        }
        }
    }

    private void populateEnterpriseNameComboBox() {
        enterpriseNamejComboBox.removeAllItems();
        Network network = (Network) cityNamejComboBox.getSelectedItem();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            enterpriseNamejComboBox.addItem(enterprise);
            populateAdminDetailsTable(network);
        }

    }

    private void populateAdminDetailsTable(Network network) {
        DefaultTableModel model = (DefaultTableModel) adminDetailsjTable.getModel();
        model.setRowCount(0);
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            for (UserAccount ua : enterprise.getUserAccountDirectory().getUserAccountList()) {
               if(!(ua.getRole() instanceof PatientRole)){
                Object[] row = new Object[5];
                row[0] = enterprise.getName();
                row[1] = ua;
                row[2] = ua.getPerson().getPersonId();
                row[3] = ua.getUsername();
                row[4] = ua.getPassword();
                model.addRow(row);
               }
            }
        }
    }

