    private void populateNurseRequestTable() {
        DefaultTableModel model = (DefaultTableModel) nurseRequestjTable1.getModel();
        model.setRowCount(0);

        for (WorkRequest wr : patient.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof DoctorToNurse) {
                DoctorToNurse dtn = (DoctorToNurse) wr;
                Object[] row = new Object[6];
                row[0] = dtn.getPatient();
                row[1] = dtn.getInstDoc();
                row[2] = dtn.getSender().getPerson().getName();
                row[3] = dtn.getReceiver() == null ? ("N/A") : dtn.getReceiver().getPerson().getName();
                row[4] = dtn;
                row[5] = dtn.getDate();
                model.addRow(row);
            }
        }
    }

