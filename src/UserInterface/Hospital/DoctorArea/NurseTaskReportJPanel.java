    private void reqestjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reqestjButtonActionPerformed
        String inst = instructionsjTextArea.getText().trim();
        if (inst.equals("") || inst == null) {
            JOptionPane.showMessageDialog(null, "Please enter the data in the instruction section.");
            return;
        }
        DoctorToNurse dtn = new DoctorToNurse();
        dtn.setPatient(patient);
        dtn.setSender(ua);
        dtn.setStatus("sent");
        dtn.setInstDoc(inst);
        
        date = new Date();
        dtn.setDate(date);

        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (Organization.Type.Nurse.getValue() == "Nurse Organization") {
                org.getWorkQueue().getWorkRequestList().add(dtn);
            }
        }
        patient.getWorkQueue().getWorkRequestList().add(dtn);
        populateNurseRequestTable();

    }//GEN-LAST:event_reqestjButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = nurseRequestjTable1.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }
        DoctorToNurse dtn = (DoctorToNurse) nurseRequestjTable1.getValueAt(selectedRow, 4);
        ViewNurseTreatmentJPanel panel = new ViewNurseTreatmentJPanel(upc, dtn);
        upc.add("DocWorkReq", panel);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.next(upc);
        
    }//GEN-LAST:event_jButton1ActionPerformed


