    private void assignjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignjButtonActionPerformed
        int selectedRow = patientTablejTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to assign to you");
            return;
        }
        
        DoctorToNurse dtn =  (DoctorToNurse) patientTablejTable1.getValueAt(selectedRow, 7);

        if(dtn.getStatus().equals("Completed")){
            JOptionPane.showMessageDialog(null, "Patient request has already been sent.");
            return;
        }
        dtn.setReceiver(ua);
        dtn.setStatus("In-Progress");
        populatePatientTable();

        if(dtn.getReceiver() == null){
            JOptionPane.showMessageDialog(null, "Please assign the request to you first");
            return;
        }
    }//GEN-LAST:event_assignjButtonActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
           int selectedRow = patientTablejTable1.getSelectedRow();
        if(selectedRow < 0) {
           JOptionPane.showMessageDialog(null, "Please select a row first ");
           return;
        }
        
        String inj = injectionjTextField3.getText().trim();
        String comp = complicationjTextArea.getText().trim();
        int dosage;
        try {
            dosage = Integer.parseInt(dosagejTextField.getText().trim());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please enter correct value");
              return;      
        }
               if (inj == null || inj.equals("")|| comp == null || dosage < 0) {
            JOptionPane.showMessageDialog(null, "Please enter some correct value");
            return;
        }

        DoctorToNurse dtn =  (DoctorToNurse) patientTablejTable1.getValueAt(selectedRow, 7);

        
 
       if (dtn.getStatus().equals("sent")) {
            JOptionPane.showMessageDialog(null, "Please assign the patient request to you first");
            return;                             
        }           
         if(dtn.getStatus().equals("Completed")){
             JOptionPane.showMessageDialog(null, "Patient data has already been sent");
             return;
         }   
         
         dtn.setInjectionName(inj);
         dtn.setDoseLevel(dosage);
         dtn.setComplexities(comp);
         dtn.setStatus("Completed");
         
         
        JOptionPane.showMessageDialog(null, "Patient data added and sent");
        populatePatientTable();
    }//GEN-LAST:event_jButton1ActionPerformed


