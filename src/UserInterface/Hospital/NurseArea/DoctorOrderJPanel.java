    private void populatePatientTable() {
        DefaultTableModel model = (DefaultTableModel) patientTablejTable1.getModel();
        model.setRowCount(0);

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof DoctorToNurse) {
                DoctorToNurse dtn = (DoctorToNurse) wr;
                Object[] row = new Object[10];
                row[0] = dtn.getReqId();
                row[1] = dtn.getPatient().getName();
                row[2] = dtn.getInjectionName() == null? ("N/A") : dtn.getInjectionName();
                row[3] = dtn.getDoseLevel();
                row[4] = dtn.getComplexities() == null? ("N/A") : dtn.getComplexities();
                row[5] = dtn.getSender().getPerson().getName();
                row[6] = dtn.getReceiver() == null? ("N/A") : dtn.getReceiver().getPerson().getName();
                row[7] = dtn;
                row[8] = dtn.getInstDoc();
                row[9] = dtn.getDate();
                model.addRow(row);
            }
        }
    }

