    private void addOrganizationjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrganizationjButtonActionPerformed
        Type type = (Type) organizationOptionjComboBox.getSelectedItem();
        if (type == null) {
            JOptionPane.showMessageDialog(null, "Please select an valid organization");
            return;
        }
        Boolean result = enterprise.getOrganizationDirectory().searchIfPresent(type);
        if(result == true){
            JOptionPane.showMessageDialog(null, "Organization already added in the enterprise");
            return;
        }

        enterprise.getOrganizationDirectory().addOrganization(type);
        populateOrganizationTable();

    }//GEN-LAST:event_addOrganizationjButtonActionPerformed

    private void backjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backjButtonActionPerformed
        upc.remove(this);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.previous(upc);
    }//GEN-LAST:event_backjButtonActionPerformed

    private void removejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removejButtonActionPerformed
        int selectedRow = organizationjTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to remove");
            return;
        }
        Organization organization = (Organization) organizationjTable.getValueAt(selectedRow, 1);
        enterprise.getOrganizationDirectory().getOrganizationList().remove(organization);
        populateOrganizationTable();
    }//GEN-LAST:event_removejButtonActionPerformed

