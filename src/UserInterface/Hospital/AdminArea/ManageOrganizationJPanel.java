    private void populateOrganizationComboBox() {
        organizationOptionjComboBox.removeAllItems();
        for (Type type : Organization.Type.values()) {
            if ((type.equals(Type.Doctor)) || (type.equals(Type.Lab))|| (type.equals(Type.Nurse))) {
                organizationOptionjComboBox.addItem(type);
            }
        }
    }

    private void populateOrganizationTable() {
        DefaultTableModel model = (DefaultTableModel) organizationjTable.getModel();
        model.setRowCount(0);
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            Object[] row = new Object[3];
            row[0] = enterprise.getName();
            row[1] = organization;
            row[2] = organization.getOrganizationID();
            model.addRow(row);
        }
    }

