    private void removejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removejButtonActionPerformed
        Organization org = null;
        int selectedRow = employeejTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to remove");
            return;
        }
        Employee employee = (Employee) employeejTable.getValueAt(selectedRow, 1);
        for (Organization org1 : enterprise.getOrganizationDirectory().getOrganizationList()) {
            org1.getPersonDirectory().getEmployeeDirectory().getEmployerList().remove(employee);
            org = org1;
        }
        populateEmployeeTable(org);
    }//GEN-LAST:event_removejButtonActionPerformed

    private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
        int selectedRow = employeejTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row to remove");
            return;
        }
        Employee emp = (Employee) employeejTable.getValueAt(selectedRow, 1);
        UpdateEmployeeData panel = new UpdateEmployeeData(upc, emp);
        upc.add("CreateAuth", panel);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.next(upc);
    }//GEN-LAST:event_updatejButtonActionPerformed


