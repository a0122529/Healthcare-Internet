    private void populateEmployeeTable(Organization organization) {
        model = (DefaultTableModel) employeejTable.getModel();
        model.setRowCount(0);

        for (Employee employee : organization.getPersonDirectory().getEmployeeDirectory().getEmployerList()) {
            Object[] row = new Object[4];
            row[0] = organization;
            row[1] = employee;
            row[2] = employee.getPersonId();
            row[3] = employee.getLocation().getCity();
            model.addRow(row);
        }
    }

    private void populateOrganizationComboBox2() {
        organizationOptionjComboBox.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            organizationOptionjComboBox.addItem(org);
            populateEmployeeTable(org);
        }
    }

