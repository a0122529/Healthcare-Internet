    private void populateSearchPatientData() {
        DefaultTableModel model = (DefaultTableModel) searchedPatientDatajTable.getModel();
        model.setRowCount(0);
        
        Object[] row = new Object[7];
        row[0] = patient;
        row[1] = patient.getSex();
        row[2] = patient.getAge();
        row[3] = patient.getPatientId();
        row[4] = patient.getLocation().getCity();
        row[5] = patient.getPrimaryDoc();
        row[6] = patient.getPrimaryDocId();
        model.addRow(row); 
        }

