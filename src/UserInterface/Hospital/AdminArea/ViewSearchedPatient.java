    private void sendToDocjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendToDocjButtonActionPerformed
        afp.setPatient(patient);
        int selectedRow = searchedPatientDatajTable.getSelectedRow();
//        if(selectedRow < 0) {
//            JOptionPane.showMessageDialog(null, "Please select a row to send the search patient");
//            return;
//        }

        Patient patient = (Patient) searchedPatientDatajTable.getValueAt(selectedRow, 0);
      //  int docId = patient.getPrimaryDocId();
        Employee doctor = null;
        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            doctor = organization.getPersonDirectory().getEmployeeDirectory().searchEmp(patient.getPrimaryDocId()); 
            if(doctor != null) {
                break;
            }
        }

        doctor.getWorkQueue().getWorkRequestList().add(afp);  
        
        JOptionPane.showMessageDialog(null, "Request has been sent to " + doctor.getName());
        
    }//GEN-LAST:event_sendToDocjButtonActionPerformed


