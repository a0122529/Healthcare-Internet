    public void populatePatientList(Drug drug) {
        patientListjComboBox.removeAllItems();
        for (Patient patient : ecosystem.getPersonDirectory().getPatientDirectory().getPatientList()) {
            Patient patient1 = (Patient) ua.getPerson();
            if(ua.getPerson() instanceof Patient){
            if(patient.getPatientId() != patient1.getPatientId()){
            for (VitalSignCheck vsc : patient.getVitalCheckHistory().getVitalCheckList()) {
                if (vsc.getDrug().getDrugName().equals(drug.getDrugName())) {
                    patientListjComboBox.addItem(patient);
                         }
                     }
                }
            }
        }        
    }

