    private void sendjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendjButtonActionPerformed
        Drug drug = (Drug) drugNamejComboBox.getSelectedItem();
        Patient patient = (Patient) patientListjComboBox.getSelectedItem();
        String message =  enterMessagejTextArea.getText();
        
        if(drug == null || patient == null || message.equals("") || message == null){
            JOptionPane.showMessageDialog(null, "Please fill all the values before sending a message");
            return;
        }
        PatientToPatient ptop = new PatientToPatient();
        ptop.setSender(ua);
        ptop.setMessage(message);
        patient.getWorkQueue().getWorkRequestList().add(ptop);
    }//GEN-LAST:event_sendjButtonActionPerformed


