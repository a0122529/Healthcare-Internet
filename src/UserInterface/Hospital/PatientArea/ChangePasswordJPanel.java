    private void submitjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitjButtonActionPerformed
        char[] newPass1 = newPasswordjTextField.getPassword();
        char[] confirmPass1 = confirmPasswordjTextField.getPassword();
        String newPass = String.valueOf(newPass1).trim();
        String confirmPass = String.valueOf(confirmPass1).trim();
        
        if(newPass.equals("") || newPass == null || confirmPass.equals("") || confirmPass == null){
            JOptionPane.showMessageDialog(null, "Please enter the in both the fields");
            return;
        }
        if(newPass.equals(confirmPass)){
            ua.setPassword(newPass);
            JOptionPane.showMessageDialog(null, "Congratulations, password has been updated");
        } else {
            JOptionPane.showMessageDialog(null, "Password do not match, please re-enter password");
            return;
        }
    }//GEN-LAST:event_submitjButtonActionPerformed

